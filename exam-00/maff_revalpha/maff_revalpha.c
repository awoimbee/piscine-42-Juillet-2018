#include <unistd.h>

int	main(void)
{
	char	a;
	char	b;
	int		demi;

	a = 'z';
	b = 'Z';
	demi = 1;
	while (a >= 'a' && b >= 'A')
	{
		if (demi == 1)
			write(1, &a, 1);
		else
			write(1, &b, 1);
		--a;
		--b;
		demi *= -1;
	}
	a = '\n';
	write(1, &a, 1);
	return (0);
}
