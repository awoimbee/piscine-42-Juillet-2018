#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	char	*c;

	if (argc == 2)
	{
		i = -1;
		while (argv[1][++i])
		{
			c = &argv[1][i];
			if ('a' <= *c && *c <= 'z')
			{
				*c -= 32;
				write(1, c, 1);
			}
			else if ('A' <= *c && *c <= 'Z')
			{
				*c += 32;
				write(1, c, 1);
			}
			else
				write(1, c, 1);
		}
	}
	write(1, "\n", 1);
}
